#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	while (lst)
	{
		i++;
		lst = lst->next;
	}
	return (i);
}

/* 	tant que lst n'est pas egal a NULL je continue d'attribuer a ma 
	lst la valeur de lst->next qui pointe vers null */